// This program is to print the number in various formats

# include <iostream>

using namespace std;

int main () {

cout	<< "A number 55 in decimal: "
	<< dec << 55 << endl;
cout	<< "A number 55 in octal: "
	<< oct << 55 << endl;
cout	<< "A number 55 in hex: "
	<< hex << 55 << endl;
cout	<< "A number 3.14159 in float: "
	<< 3.14159 << endl;
cout	<< "A non-print character <escape>: "
	<< char(20) << endl;

return (0);
}
